package ListDemo;

public class MysingleList {

    //链表(单向，双向，带头，不带头，循环，非循环)
    //节点
    //1.数值域，当前节点的地址
    //2.next域，下一个节点的地址



    static class ListNode{
        //什么时候设计成内部类
        //链表一个个节点这种

        public int value;//数值域
        public ListNode next;//存储下一个节点的地址

        public ListNode(int val){
            this.value = value;
        }
    }
}
